<?php

namespace App\Http\Resources\Settings\Payroll;

use Illuminate\Http\Resources\Json\ResourceCollection;

class EmployeeBaseSalaryListResource extends ResourceCollection
{
    /**
     * Transform the resource collection into an array.
     *
     * @param  \Illuminate\Http\Request
     * @return array
     */
    public function toArray($request)
    {
        return [
            'data' => $this->transformCollection($this->collection),
            'meta' => [
                "success" => true,
                "message" => "Success get employee base salary lists",
                'pagination' => $this->metaData()
            ]
        ];
    }

    private function transformData($data)
    {
        return [
            'id' => $data->id,
            'employee_name' => $data->employee_detail->user_detail->name,
            'employee_id' => $data->employee_id,
            'salary_type' => $data->type,
            'type' => $data->type,
            'amount_formatted' => $data->amount_formatted,
            'amount' => (int)$data->amount
        ];
    }

    private function transformCollection($collection)
    {
        return $collection->transform(function ($data) {
            return $this->transformData($data);
        });
    }

    private function metaData()
    {
        return [
            "total" => $this->total(),
            "count" => $this->count(),
            "per_page" => (int)$this->perPage(),
            "current_page" => $this->currentPage(),
            "total_pages" => $this->lastPage(),
            "links" => [
                "next" => $this->nextPageUrl()
            ],
        ];
    }
}
